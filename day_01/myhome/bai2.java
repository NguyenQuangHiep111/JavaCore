package day_01.myhome;

public class bai2 {
    public static void main(String[] args) {
        int i = 20;
        System.out.println("i++ có giá trị: "+ (i++));
//        i++: Tăng giá trị i lên 1 nhưng trả về giá trị cũ i.
        System.out.println("++i có giá trị: "+ (++i));
//        ++i: tăng giá trị i lên 1 và trả về giá trị mới i + 1.
    }
}
